            if(infected[cowy[i]]) {num_handshakes[cowy[i]]++;}
            if(num_handshakes[cowx[i]] <= K && infected[cowx[i]]) {infected[cowy[i]] = true;}
            if(num_handshakes[cowy[i]] <= K && infected[cowy[i]]) {infected[cowx[i]] = true;}
